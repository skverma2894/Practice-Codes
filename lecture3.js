// Operations on numbers

var a = 5.7;
console.log(a);

// Adding, Substracting, Multiplying and Dividing a number

var e = 5 + 5;
console.log(e);
var b = 5 - 5;
console.log(b);
var c = 5 * 5;
console.log(c);
var d = 5 / 5;
console.log(d);

//incrementing and decrementing a varaible

d++;
console.log("this is incremented d");
console.log(d);

e--;

console.log("this is decremented e");
console.log(e);

//incrementing or decrementing by any number you want.

var j = 100;

j -= 55;

// here concatenation operator is also used.
console.log("this is 55 minus j " + j);

j += 45;

console.log("this is 45 added to modified j " + j);

//pre increment pre decrement operator and storing it to some variable

var g = ++d;
console.log(g);

var h = --d;
console.log(h);

//we can directly insert operators in the console.log

console.log(15 % h); //stores remainder
